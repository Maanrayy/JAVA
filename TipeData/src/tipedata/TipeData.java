
package tipedata;


public class TipeData {

   
    public static void main(String[] args) {
        System.out.println("6+5"); /*Adanya Tanda kutip("")membuat 6+5 ditampilkan sebagai Output */
        System.out.println(6+5); /* Tanpa adanya tanda kutip ("") maka 6+5 Dianggap Sebagai Sistem Operasi */
    }
    
}
