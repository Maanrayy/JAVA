package praktek_tipedata;


public class Praktek_tipedata {

   
    public static void main(String[] args) {
        String nama ="Ronaldino";
        System.out.println("*******Biodata Siswa*******");
        System.out.println("Nama \t= " + nama);
        
        int umur = 15;
        System.out.println("Umur \t= " + umur + " Tahun");
        
        
        float berat = (float) 50.7;  
        System.out.println("Berat \t= " + berat + " kg");
        
        double tinggi = 160.89;
        System.out.println("Tinggi \t= " + tinggi + " cm");
        
        char gender = 'L';
        System.out.println("Gender \t= " + gender);
        
        System.out.println("********Program selesai******");
        
    }
    
}
